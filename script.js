function showUnhealthy() {
  var x = document.getElementById("Unhealthy Plant");
  if (x.style.display === "none") {
    x.style.display = "block";
  } else {
    x.style.display = "none";
  }
}

function showHealthy() {
  var x = document.getElementById("Healthy Plant");
  if (x.style.display === "none") {
    x.style.display = "block";
  } else {
    x.style.display = "none";
  }
}